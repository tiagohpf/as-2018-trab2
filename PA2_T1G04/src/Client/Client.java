    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // create a socket
            mySocket = new Socket(jTextField1.getText(), Integer.parseInt(jTextField2.getText()));
            // socket's output stream
            out = new PrintWriter(mySocket.getOutputStream(), true);
            // socket's input stream
            in = new BufferedReader(new InputStreamReader(mySocket.getInputStream()));
            jTextArea1.append("Connection is established with the Load Balancer\n");
            jButton1.setEnabled(false);
            id = in.readLine();
            jTextArea1.append("id = " + id + "\n");
            requestId = 0;
        } catch (UnknownHostException e) {
            jTextArea1.append("Don't know about host\n");
        } catch (IOException e) {
            jTextArea1.append("Couldn't get I/O for the connection to host\n");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Thread request = new Thread() {
            public void run() {
                if (jTextField3.getText().equals("") || jTextField4.getText().equals("")) {
                    // TODO: Change to message box
                    jTextArea1.append("Insert all fields.\n");
                } else {
                    requestId++;
                    out.println(jTextField3.getText() + " " + jTextField4.getText());
                    out.println(requestId);
                    jTextArea1.append("Request: " + id + " | " + requestId 
                            + " | 01 | " + jTextField3.getText() + " | " + jTextField4.getText() + "\n");
                    String result = null;
                    try {
                        result = in.readLine();
                    } catch (IOException ex) {
                        Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    jTextArea1.append(result + "\n");
                }
            }
        };
        request.start();
    }//GEN-LAST:event_jButton2ActionPerformed

